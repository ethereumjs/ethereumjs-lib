module.exports = function (op, full){

  var opcode = codes[op];

  if(full){
    if(opcode === 'LOG'){
       opcode += op - 0x9f;
    }

    if(opcode === 'PUSH'){
       opcode += op - 0x5f;
    }

    if(opcode === 'DUP'){
       opcode += op - 0x7f;
    }

    if(opcode === 'SWAP'){
       opcode += op - 0x8f;
    }
  }

  return opcode;
};
